#include "ft_printf.h"

int	ft_printstr(const char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
	{
		ft_printchar(s[i]);
		i++;
	}
	return (i);
}
